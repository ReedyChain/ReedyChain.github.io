<h1>BLACK OPS II</h1>
<p></p>
Fue lanzado el 13 de noviembre de 2012 para PlayStation 3, Xbox
360 y Microsoft Windows, el 18 de noviembre de 2012 en América del Norte, y finalmente el 30 de noviembre 
de 2012 en Europa y Australia para la Wii U.1​2​3​ Square Enix lanzó el juego para el mercado japonés el 22
 de noviembre de 2012 como una versión subtitulada. Una versión de voz doblada al idioma japonés fue lanzado 
por separado el 20 de diciembre de 2012. El guion de esta versión fue traducido del inglés por Zenigame Nakamoto.
Call of Duty: Black Ops II es el noveno juego de la saga Call of Duty y una secuela del juego de 2010 Call of Duty: Black Ops.
 El juego fue lanzado en 16.000 tiendas en todo el mundo a la medianoche del 13 de noviembre de 2012. Consiguió un récord de
  ventas por aquel entonces de 500 millones de dólares,4​ solo superado por Grand Theft Auto V.
Call of Duty: Black Ops II es el segundo juego de la saga Call of Duty en presentar la futura tecnología de la guerra, y el 
primero en presentar historias de ramificación impulsados por elección del jugador, así como la selección de armas antes de comenzar
 las misiones del modo historia. También ofrece una opción de visualización en 3D.5</p>
