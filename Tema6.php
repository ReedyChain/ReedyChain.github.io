    <h1>
        POLEMICA ESCENA EN EL REMAKE DE MW3 ¿SE VOLVERA A REPETIR?
    </h1>
    <P>
    Activision publicó el primer avance de Call of Duty: Modern Warfare 3, la próxima entrega del juego de disparos en primera persona.
     Aunque la atención se ha centrado en los agregados al multijugador o los nuevos escenarios, el tráiler muestra algunos segundos
      del No Russian (Nada de ruso), la recreación de una de las escenas más polémicas de los videojuegos.

La historia de Call of Duty: Modern Warfare 3 da continuidad a los sucesos ocurridos en el juego anterior. 
Si hacemos memoria, la escena post-créditos de Call of Duty: Modern Warfare 2 daba pistas sobre el regreso del No Russian. Una persona a bordo de un avión ensambla una pistola esperando el visto bueno para iniciar la carnicería

El nuevo tráiler muestra los primeros segundos del ataque, cuando el individuo se levanta del asiento tras 
recibir el mensaje No Russian. El atacante camina por el pasillo con pistola en mano, mientras se escuchan los 
gritos de "agáchense" en el fondo. En una fracción de segundo aparece una mujer sujetando un objeto (posiblemente
 una sobrecargo) antes de que el personaje apunte con el arma.
